func countContinueMonths(pensions []Pension) int {
	var continuesMonth int
	pensionsLen := len(pensions)
	if pensionsLen == 0 {
		return continuesMonth
	}
	if pensionsLen == 1 {
		return continuesMonth + 1
	}
	for i := pensionsLen - 1; i-1 >= 0; i-- {
		month, _ := time.Parse("200601", pensions[i].Aae003)

		montAfter, _ := time.Parse("200601", pensions[i-1].Aae003)

		monthAddOne := month.AddDate(0, 1, 0)

		if monthAddOne == montAfter {
			continuesMonth++
		} else {
			continuesMonth = 0
		}
	}

	return continuesMonth + 1
}

