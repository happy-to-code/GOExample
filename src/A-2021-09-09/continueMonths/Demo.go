func countContinueMonths2(pensions []Pension) int {
	var continuesMonth int
	pensionsLen := len(pensions)
	if pensionsLen == 0 {
		return continuesMonth
	}

	// 参数的第一个值
	month, _ := time.Parse("200601", pensions[0].Aae003)
	now := time.Now()

	// 当前月已经有数据
	if now.Year() == month.Year() && now.Month() == month.Month() {
		// 截取数据
		pensions = pensions[1:]
	}
	if pensionsLen == 0 {
		return continuesMonth
	}

	// 当前月没有数据
	month, _ = time.Parse("200601", pensions[0].Aae003)
	if now.Year() > month.Year() {
		return continuesMonth
	} else if now.Year() == month.Year() {
		monthAddOne := month.AddDate(0, 1, 0)
		if now.Month() > monthAddOne.Month() {
			return continuesMonth
		}
	}

	// 重新计算集合的长度
	pensionsLen = len(pensions)
	for i := 0; i+1 <= pensionsLen-1; i++ {
		// if i == 0 {
		// 	month, _ = time.Parse("200601", pensions[i].Aae003)
		// }
		montAfter, _ := time.Parse("200601", pensions[i+1].Aae003)

		monthDelOne := month.AddDate(0, -1, 0)
		if monthDelOne == montAfter {
			continuesMonth++
			month = monthDelOne
		} else {
			return continuesMonth + 1
		}
	}

	return continuesMonth + 1
}
